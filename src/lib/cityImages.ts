function hashString(str: string): number {
  let hash = 0;
  for (let i = 0; i < str.length; i++) {
    hash = ((hash << 5) - hash) + str.charCodeAt(i);
    hash |= 0;
  }
  return Math.abs(hash);
}

export function getCityImage(cityName: string, seed?: string): string {
  const images = CITY_IMAGES[cityName];
  if (images) {
    const index = hashString(seed || cityName) % images.length;
    return images[index];
  }

  for (const [key, imgs] of Object.entries(CITY_IMAGES)) {
    if (cityName.includes(key) || key.includes(cityName)) {
      const index = hashString(seed || cityName) % imgs.length;
      return imgs[index];
    }
  }

  const index = hashString(cityName) % DEFAULT_IMAGES.length;
  return DEFAULT_IMAGES[index];
}

